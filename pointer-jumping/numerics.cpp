#include <iostream>

using namespace std;

int main() {

  int example_bool;
  cout << "size of bool: " << sizeof(example_bool) << endl;

  int example_char;
  cout << "size of char: " << sizeof(example_char) << endl;

  int example_int;
  cout << "size of int: " << sizeof(example_int) << endl;

  long example_long;
  cout << "size of long: " << sizeof(example_long) << endl;

  long long example_long_long;
  cout << "size of long long: " << sizeof(example_long_long) << endl;


  float example_float;
  cout << "size of float: " << sizeof(example_float) << endl;


  double example_double;
  cout << "size of double: " << sizeof(example_double) << endl;

  double example_long_double;
  cout << "size of long double: " << sizeof(example_long_double) << endl;  

}
